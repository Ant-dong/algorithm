package top.zhenganwen.learn.algorithm.leetcode.tree;

import java.util.List;

/**
 * @author zhenganwen
 * @date 2019/11/20 17:38
 */
public class Node {
    public int        val;
    public List<Node> children;

    public Node() {
    }

    public Node(int _val) {
        val = _val;
    }

    public Node(int _val, List<Node> _children) {
        val = _val;
        children = _children;
    }
}